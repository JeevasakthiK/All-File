package org.inm;

public class FullstacksJava {
	private void coreJava() {
		System.out.println("Core JAVA");
	}
	private void advanceJava() {
		System.out.println("Advance JAVA");
	}
	private void javaScript() {
		System.out.println("JAVA Script");	
	}
	private void springFramework() {
		System.out.println("Spring Framework");
		
	}
	public static void main(String[] args) {
		System.out.println("*** Fullstacks JAVA ***");
		FullstacksJava f = new FullstacksJava();
		f.coreJava();
		f.advanceJava();
		f.javaScript();
		f.springFramework();
	}

}
